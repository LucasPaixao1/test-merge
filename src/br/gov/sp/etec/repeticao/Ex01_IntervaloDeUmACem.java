package br.gov.sp.etec.repeticao;

public class Ex01_IntervaloDeUmACem {

	/*
	 * 1. Desenvolva uma classe java chamada IntervaloDeUmACem que exiba os números
	 * de 1 a 100 na tela em ordem decrescente.
	 */
	public static void main(String[] args) {

		int cont = 100;
		
		
		while (cont != 0) {

			System.out.println(cont);
			cont--;

		}
		
		
		System.exit(0);

	}

}
