package br.gov.sp.etec.trabalho;

public class Ex04_Padrao4 {

	public static void main(String[] args) {
		for (int cont = 1; cont <= 5; cont++) {
			switch(cont) {
			case 1:
				System.out.println("*");
				break;
			case 2:
				System.out.println("**");
				break;
			case 3:
				System.out.println("***");
				break;
			case 4:
				System.out.println("****");
				break;
			case 5:
				System.out.println("*****");
				break;
			
			}
			
		

		}
		System.exit(0);
	}

}
